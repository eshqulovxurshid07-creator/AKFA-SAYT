/* Active language (default: uz) */
let activeLang = localStorage.getItem('akfa_lang') || 'uz';

function t(key){ return (LANGS[activeLang][key] || LANGS['uz'][key] || key); }

function setLang(lang){
  activeLang = lang;
  localStorage.setItem('akfa_lang', lang);
  applyLang();
}

function applyLang(){
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const key = el.dataset.i18n;
    const val = t(key);
    if(el.tagName==='INPUT'||el.tagName==='TEXTAREA') el.placeholder=val;
    else el.innerHTML=val;
  });
  document.querySelectorAll('[data-i18n-arr]').forEach(el=>{
    const key = el.dataset.i18nArr;
    const idx = parseInt(el.dataset.i18nIdx||0);
    const arr = LANGS[activeLang][key]||LANGS['uz'][key]||[];
    el.textContent=arr[idx]||'';
  });
  document.querySelectorAll('.lang-btn').forEach(b=>{
    b.classList.toggle('active', b.dataset.lang===activeLang);
  });
  document.documentElement.lang = activeLang;
}

document.addEventListener('DOMContentLoaded',()=>{
  applyLang();
  document.querySelectorAll('.lang-btn').forEach(b=>{
    b.addEventListener('click',()=>setLang(b.dataset.lang));
  });
});
